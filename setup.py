        await client.start()
        
        if await client.get_me():
            session_string = await client.export_session_string()
            print("\\n" + "=" * 50)
            print("âœ… SESSION GENERATED SUCCESSFULLY!")
            print("=" * 50)
            print("Copy the following session string and use it in setup:")
            print("\\n" + session_string)
            print("\\n" + "=" * 50)

